n1 = int(input('Fala um numero ai pra eu ver se ele é par ou ímpar '))
if n1 % 2 == 0:
    print('o numero {} é par '.format(n1))
    print('ele é par porque quando ele foi dividido por 2 o resto da divisão deu 0')
else:
    print('O numero {} é ìmpar'.format(n1))
    print('ele é ímpar por que quando ele foi dividido por 2 o resto da divisão nao deu 0')
